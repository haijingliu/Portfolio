                 <h2>HoneyComb</h2>
                 <h3>User Experience / Interaction Design｜2013</h3>
                 <p1><p>“Staying in touch with current events at home, to your commute, to work or school.”</p>
                 <p>If news is tagged geographically, how can it be used to stay in touch with friends and family?</p> From the hyperlocal to national and global, how can people stay informed about the neighborhoods and cities they frequently switch. With a visualized mapping of this information around a user,
                 we want to create centrical experiences to better understand user-defined landscapes.</p>
                 Show categorically ordered news ie. any news aggregator homepage like google news. While these types of aggregators provide an organized approach for searching for news, they lack a means of personally relating to these updates.</p>
                 News that is contextually aware, not only allows user to stay up to date with the information that matters,
                 but also curates a narrative of the users environments.</p>
                 </p1>

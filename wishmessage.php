                 <div id="work_articles">
                 <p>Users, networks, information have given a basic structure of the network dynamics; it forms a society.
                 Internet its self is obviously a huge database.manifested a planetary meta-organism. Virtual human behaviors have been building up the community eternally. Virtual communities
                 that has improved the efficiency and quality of internet communications and also has triggered complex emotions and real consequences constantly.
                 In this case, I am studying the physical boundaires of virtual networks, and try to explore if there’s a possible way to reform the user generated data in a sensible entity.
                 <p> Wish Message is a communication interface that enables the user to literally grab wishes from the Internet(twitter) worldwidely. 
                 The bamboo copter in east asia,contains a metaphor of good blessing.</p>

                 <p>Instead of the touch or click triggering process,the interaction system allows the user who constantly in contact with modern HCI for
                 many efficient purpose to (re)gain a physically playful experience, plus, subverts the perception between electronic devices and users.
                 In critiquing the phenomena of mordern HCI and a general usage of social networking-tie, this interface want to emphasizes a physical or haptic perception with data,
                 hence, provides participants a co-related emotional experience that beyond their own networks.<p>
                <p>Interface Material: Basswood, Vinyl.</p>
